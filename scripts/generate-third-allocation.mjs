// Generate lib/bracket-third-allocation.generated.ts — the FIFA World Cup 2026
// best-third Round-of-32 allocation (Annex C). Fetches the canonical Wikipedia
// template (a faithful transcription of Annex C) and parses its wikitext.
//
//   node scripts/generate-third-allocation.mjs
//
// The committed lib/bracket-third-allocation.generated.ts is the source of
// truth; tests/bracket-third-allocation.test.ts guards its integrity. Re-run
// this only to refresh from source — it refuses to write unless it parses
// exactly 495 rows that all pass the same integrity checks the test enforces.

import { writeFileSync } from "node:fs";

const SRC =
  "https://en.wikipedia.org/wiki/Template:2026_FIFA_World_Cup_third-place_table?action=raw";

// R32 third-place slots in FIFA's published left-to-right order, by the group
// winner each faces, with the candidate groups whose 3rd can fill them.
const SLOT_WINNERS = ["A", "B", "D", "E", "G", "I", "K", "L"];
const CANDIDATES = {
  A: "CEFHI", B: "EFGIJ", D: "BEFIJ", E: "ABCDF",
  G: "AEHIJ", I: "CDFGH", K: "DEIJL", L: "EHIJK",
};

const res = await fetch(SRC);
if (!res.ok) throw new Error(`fetch failed: ${res.status} ${res.statusText}`);
const wiki = await res.text();

// Rows are separated by `|-`. A data row, once its lines are joined, contains
// exactly 8 bold qualifying group letters ('''X''') and 8 assignment cells
// (3X) in slot order.
const table = {};
for (const block of wiki.split(/^\|-\s*$/m)) {
  const flat = block.replace(/\n/g, " ");
  const qualified = [...flat.matchAll(/'''([A-L])'''/g)].map((m) => m[1]);
  const assigned = [...flat.matchAll(/\b3([A-L])\b/g)].map((m) => m[1]);
  if (qualified.length !== 8 || assigned.length !== 8) continue;
  table[[...qualified].sort().join("")] = assigned.join("");
}

// Integrity — same invariants as the test; refuse to emit garbage.
const rows = Object.entries(table);
if (rows.length !== 495) {
  throw new Error(`expected 495 rows, parsed ${rows.length}`);
}
for (const [k, v] of rows) {
  if ([...k].sort().join("") !== [...v].sort().join("")) {
    throw new Error(`row ${k}: value ${v} is not a permutation of the key`);
  }
  SLOT_WINNERS.forEach((w, i) => {
    if (!CANDIDATES[w].includes(v[i])) {
      throw new Error(`row ${k}: slot 1${w} got ${v[i]}, not a candidate`);
    }
  });
}

const body = rows
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([k, v]) => `  ${k}: "${v}",`)
  .join("\n");

const out = `// FIFA World Cup 2026 - best third-placed teams Round-of-32 allocation (Annex C).
// Source: Wikipedia Template:2026_FIFA_World_Cup_third-place_table (transcription of FIFA Annex C).
// Generated by scripts/generate-third-allocation.mjs — do not edit by hand.
// Key  = the 8 qualifying third-place groups, sorted alphabetically (e.g. "ABCDEFGH").
// Value= 8 group letters giving the third-place team that faces each group winner,
//        in slot order: [1A, 1B, 1D, 1E, 1G, 1I, 1K, 1L].
export const THIRD_PLACE_SLOT_ORDER = ["1A","1B","1D","1E","1G","1I","1K","1L"] as const;
export const THIRD_PLACE_ALLOCATION: Record<string, string> = {
${body}
};
`;

writeFileSync(
  new URL("../lib/bracket-third-allocation.generated.ts", import.meta.url),
  out,
);
console.log(`wrote lib/bracket-third-allocation.generated.ts (${rows.length} rows)`);
